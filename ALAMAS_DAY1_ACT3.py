Sentence = input('Enter a string: ')

countAa = Sentence.lower().count('a')
countEe = Sentence.lower().count('e')
countIi = Sentence.lower().count('i')
countOo = Sentence.lower().count('o')
countUu = Sentence.lower().count('u')

print(f'Letter a count is: {countAa}')
print(f'Letter e count is: {countEe}')
print(f'Letter i count is: {countIi}')
print(f'Letter o count is: {countOo}')
print(f'Letter u count is: {countUu}')
